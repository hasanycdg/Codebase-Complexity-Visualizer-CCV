import fs from "node:fs";
import path from "node:path";

const ROOT = process.cwd();
const SCAN_DIRS = ["apps", "packages"];
const SOURCE_EXTENSIONS = new Set([".ts", ".tsx", ".js", ".jsx", ".rs"]);
const SKIP_DIRS = new Set(["node_modules", "dist", "target", ".git", ".next", ".turbo"]);

const emojiRegex = /\p{Extended_Pictographic}/u;
const aiMarkerRegex = /(as an ai|generated by ai|ai-generated|ai generated|chatgpt|copilot|llm)/i;

const findings = [];

function shouldSkipDir(dirName) {
  return SKIP_DIRS.has(dirName);
}

function shouldScanFile(filePath) {
  const extension = path.extname(filePath);
  return SOURCE_EXTENSIONS.has(extension);
}

function walk(dirPath) {
  const entries = fs.readdirSync(dirPath, { withFileTypes: true });
  for (const entry of entries) {
    const entryPath = path.join(dirPath, entry.name);

    if (entry.isDirectory()) {
      if (shouldSkipDir(entry.name)) {
        continue;
      }
      walk(entryPath);
      continue;
    }

    if (!entry.isFile() || !shouldScanFile(entryPath)) {
      continue;
    }

    scanFile(entryPath);
  }
}

function scanFile(filePath) {
  const content = fs.readFileSync(filePath, "utf8");
  const lines = content.split(/\r?\n/);

  for (let i = 0; i < lines.length; i += 1) {
    const line = lines[i];

    if (emojiRegex.test(line)) {
      findings.push(`${relative(filePath)}:${i + 1} emoji found`);
    }

    const lineCommentMatch = line.match(/^\s*\/\/(.*)$/);
    if (lineCommentMatch && aiMarkerRegex.test(lineCommentMatch[1])) {
      findings.push(`${relative(filePath)}:${i + 1} AI marker in comment`);
    }
  }

  const blockCommentRegex = /\/\*[\s\S]*?\*\//g;
  let match;
  while ((match = blockCommentRegex.exec(content)) !== null) {
    const block = match[0];
    if (!aiMarkerRegex.test(block)) {
      continue;
    }

    const before = content.slice(0, match.index);
    const lineNumber = before.split(/\r?\n/).length;
    findings.push(`${relative(filePath)}:${lineNumber} AI marker in block comment`);
  }
}

function relative(filePath) {
  return path.relative(ROOT, filePath);
}

for (const dir of SCAN_DIRS) {
  const fullPath = path.join(ROOT, dir);
  if (fs.existsSync(fullPath)) {
    walk(fullPath);
  }
}

if (findings.length > 0) {
  console.error("Code tone check failed:\n");
  for (const finding of findings) {
    console.error(`- ${finding}`);
  }
  process.exit(1);
}

console.log("Code tone check passed.");
